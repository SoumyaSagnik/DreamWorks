package Project;

public class Pojo {
	private String web;
	private String search;
	private String link;

	public Pojo(String web, String search, String link) {
		super();
		this.web = web;
		this.search = search;
		this.link = link;
	}

	public String getWeb() {
		return web;
	}

	public String getSearch() {
		return search;
	}

	public String getLink() {
		return link;
	}
}
